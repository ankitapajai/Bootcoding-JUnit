package com.bootcoding.junit.Array;

public class ArraySum {
    public int getSum(int numbers[]){
        int sum = 0;
        for(int n : numbers){
            sum = sum + n;
        }
        return sum;
    }

    //print maximum elements...
    public int getMaxElement(int numbers[]){
        int sum = 0;
        for(int n1 : numbers){
            sum = sum + n1;
        }
        return sum;
    }

    //print even elements in an array...
    public int evenElement(int numbers[]){
        int sum = 0;
        for(int n2 : numbers){
            sum = sum + n2;
        }
        return sum;
    }
}
